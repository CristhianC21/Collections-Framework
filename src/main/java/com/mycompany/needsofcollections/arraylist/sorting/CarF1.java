public class CarF1 {
    private int driverNumber;
    private String driverName;
    private String driverScuderia;
    private int driverWins;
    
    public CarF1(int driverNumber, String driverName, String driverScuderia, int driverWins){
        super();
        this.driverNumber = driverNumber;
        this.driverName = driverName;
        this.driverScuderia = driverScuderia;
        this.driverWins = driverWins;
    }
            
    /**
     * @return the driverNumber
     */
    public int getDriverNumber() {
        return driverNumber;
    }

    /**
     * @param driverNumber the driverNumber to set
     */
    public void setDriverNumber(int driverNumber) {
        this.driverNumber = driverNumber;
    }

    /**
     * @return the driverName
     */
    public String getDriverName() {
        return driverName;
    }

    /**
     * @param driverName the driverName to set
     */
    public void setDriverName(String driverName) {
        this.driverName = driverName;
    }

    /**
     * @return the driverScuderia
     */
    public String getDriverScuderia() {
        return driverScuderia;
    }

    /**
     * @param driverScuderia the driverScuderia to set
     */
    public void setDriverScuderia(String driverScuderia) {
        this.driverScuderia = driverScuderia;
    }

    /**
     * @return the driverWins
     */
    public int getDriverWins() {
        return driverWins;
    }

    /**
     * @param driverWins the driverWins to set
     */
    public void setDriverWins(int driverWins) {
        this.driverWins = driverWins;
    }


}
